                <p class="serif">While looking to engage users with this information, it is important to locate relevant factors that are understandable, and meaningful for them to decide the extent of their relationship with these services. When using Explainable AI methodologies as a medium to make obscured algorithmic processes apparent, this project finds it imperative to look at users as new learners, informing their decisions by ways of explainability that are borrowed from pedagogy and instructional design.</p>

                <p class="serif">The project proposes to relook at users as learners, and their interactions with digital services as a ‘site for learning’. We propose Algo-Lit plug which helps users understand their preferences, available user controls on a digital service, understand what different User Interface elements mean and how they contribute in the decision making of the algorithmic processes and the entire system of decision making. The project is a speculation of explainability of AI features for end-users.</p>

