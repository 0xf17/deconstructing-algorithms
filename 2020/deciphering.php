                <p class="serif big mt-5 mb-3">What if users could interact with elements they see on the user interface, inspect the digital systems they use, and understand the algorithm-driven processes behind them?</p>

                <p class="serif">With back-end algorithmic processes becoming increasingly obscured and vastly complex, users are often unaware of the rationale employed behind the services they use. There is a call for a new kind of literacy. This project proposes ‘Algorithmic literacy’, which brings to the forefront information about algorithmic processes that automate and augment tasks users engage with on digital services. What one's agency is in algorithmic decision making and what actions they can control on a digital service are also parts of the proposed algorithmic literacy.</p>
                <p class="serif">To achieve this vision, the project identifies ‘Explainable AI’ as a methodology which inspects and explains the steps and models involved at the back-end of artificial intelligence technology and makes it apparent and understandable for users.</p>

                <div class="video-container mb-3">
